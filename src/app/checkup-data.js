/**
 * 获取某周需要做的产检
 */
export function getCheckupsForWeek(week) {
  return checkupSchedule.filter(c => week >= c.week && week <= c.weekEnd);
}

/**
 * 获取还未做的产检（在当前周之后的）
 */
export function getUpcomingCheckups(currentWeek) {
  return checkupSchedule.filter(c => c.week > currentWeek);
}

/**
 * 获取已过的产检
 */
export function getPastCheckups(currentWeek) {
  return checkupSchedule.filter(c => c.weekEnd < currentWeek);
}
