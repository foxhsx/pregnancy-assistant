/**
 * 获取最接近的饮食指南（向下取到最近的key）
 */
export function getDietGuide(week) {
  const weeks = Object.keys(dietGuide).map(Number).sort((a, b) => a - b);
  let best = weeks[0];
  for (const w of weeks) {
    if (w <= week) best = w;
    else break;
  }
  return dietGuide[best];
}
